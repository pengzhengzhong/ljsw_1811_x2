function pointInit(bArr,mInd){
	for (var i=0;i<bArr.length;i++) {
		var mPoin = bArr[i].bind;
		var mAn = bArr[i].aind;
		if(i < mInd){
			bArr[i].btn.css({"left":mAnPos[mAn].x+"px","top":mAnPos[mAn].y+"px"});
		}else{
			bArr[i].btn.css({"left":mBnPos[mPoin].x+"px","top":mBnPos[mPoin].y+"px"});
		}
	}
}

